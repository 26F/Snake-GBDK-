UINT8 X_seed = 221;

UINT8 random_UINT8() {
  X_seed = (13 * X_seed + 1) % 256;
  return X_seed;
}

#define INIT_SPEED  		5

UWORD seed;
UINT8 green_tile = 0x00;
UINT8 white_tile = 0x01;
UINT8 wait4 = INIT_SPEED;
UINT8 d;
UINT8 direction = 0;
UINT8 head = 0;
UINT8 tail = 0;
UINT8 snakesize = 1; //  seems to be limited to 59 for some reason 
UINT8 growth = 0;
