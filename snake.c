void reset_game() {
	HIDE_BKG;
	set_bkg_tiles(0,0, 20, 18, &snake_map);
	wait_vbl_done();

	snakesize = 1;
	head = 0;
	tail = 0;
	growth = 0;
	arrayloctail = 0;
	arraylochead = 0;
	flash = 0;
	direction = 0;
	snakex[0] = 10;
	snakey[0] = 9;
	arrayi[0] = 0;
	wait4 = INIT_SPEED;
	faster = 0;

	while (1) {
    food.x = random_UINT8() / 14; // so it doesn't spawn on the snake
    food.y = random_UINT8() / 14;
    if (food.x != snakex[0] || food.y != snakey[0]) {
      break;
    }
  }
  wait_vbl_done();
  SHOW_BKG;

}

void main() {

  set_bkg_data(0,137, TileData);
  set_bkg_tiles(0,0, 20, 18, MapData);
  SHOW_BKG;
  DISPLAY_ON;

  while (1) {
  	wait_vbl_done();
  	if (joypad() & J_A) {
  	waitpadup();
  	gamemode = 1;
  	break;
  }
  if (joypad() & J_B) {
  	waitpadup();
  	gamemode = 0;
  	break;
  	}
  }
  HIDE_BKG;

  

  snakex[0] = 10;
  snakey[0] = 9;
  arrayi[0] = 0; 

  while (1) {
    food.x = random_UINT8() / 14; // so it doesn't spawn on the snake
    food.y = random_UINT8() / 15;
    if (food.x != snakex[0] || food.y != snakey[0]) {
      break;
    }
  }

  set_bkg_data(0,6, sprite_data);
  set_bkg_tiles(0,0, 20,18, snake_map);
  SHOW_BKG;

  while (1) {
  	
  	if (joypad() & J_A) {
  		waitpadup();
  		reset_game();
  	}
  	

  	for (d = 0; d < wait4; d++) {
      wait_vbl_done();
    }

    kill.x = snakex[tail];
    kill.y = snakey[tail];
    next.x = snakex[head];
    next.y = snakey[head];

    rot_arrayR(arrayi, snakesize);

    if (growth) {
    	faster++;
    	if (faster == 6) {
      		if (wait4 > 2) {
      			wait4--;
      			faster = 0;
      		}
      }
      arrayloctail++;
      snakesize++;
      arrayi[arrayloctail] = arrayloctail;
      snakex[arrayloctail] = kill.x;
      snakey[arrayloctail] = kill.y;
      growth = 0;
      while(1) {
      	food.x = random_UINT8() / 14;
      	food.y = random_UINT8() / 15;
      	get_bkg_tiles(food.x, food.y, 1,1, &gettile); // this should stop food spawning on the snake
      	if (gettile != 0x00) {
      		break;
      	}
      	

      }
    }

    if (joypad() & J_LEFT && direction != 1) {
      direction = 0;
    }
    if (joypad() & J_RIGHT && direction != 0) {
      direction = 1;
    }
    if (joypad() & J_UP && direction != 3) {
      direction = 2;
    }
    if (joypad() & J_DOWN && direction != 2) {
      direction = 3;
    }

    switch(direction) {
      case 0:  {
      	if (next.x - 1 > 19) {
      		if (gamemode) {
      			reset_game();
      		}
      		else
      		{
      			snakex[tail] = 19;
      		}
      		
      	}
      	else
      	{
      		snakex[tail] = next.x - 1;
      	}
        snakey[tail] = next.y;
        break;
      }
      case 1: {
      	if (next.x + 1 > 19) {
      		if (gamemode) {
      			reset_game();
      		}
      		else
      		{
      			snakex[tail] = 0;
      		}
      		
      	}
      	else
      	{
      		snakex[tail] = next.x + 1;
      	}
        
        snakey[tail] = next.y;
        break;
      }

      case 2: {
      	if (next.y - 1 > 17) {
      		if (gamemode) {
      			reset_game();
      		}
      		else
      		{
      			snakey[tail] = 17;
      		}
      		
      	}
      	else
      	{
      		snakey[tail] = next.y - 1;
      	}
        
        snakex[tail] = next.x;
        break;
      }
      case 3: {
      	if (next.y + 1 > 17) {
      		if (gamemode) {
      			reset_game();
      		}
      		else
      		{
      			snakey[tail] = 0;
      		}
      		
      	}
      	else
      	{
      		snakey[tail] = next.y + 1;
      	}
        
        snakex[tail] = next.x;
        break;
      }
    }

    if (food.x == snakex[tail] && food.y == snakey[tail]) {
    	growth = 1;
    }

    get_bkg_tiles(snakex[tail], snakey[tail], 1,1, &gettile);
    if (gettile == 0x00 && food.x != snakex[tail] && food.y != snakey[tail]) {
    	reset_game();
    }

    flash++;
    if (flash % 3 == 0) {
      set_bkg_tiles(food.x, food.y, 1,1, &white_tile);
    }
    else
    {
      set_bkg_tiles(food.x, food.y, 1,1, &green_tile);
    }

    set_bkg_tiles(kill.x, kill.y, 1,1, &white_tile);
    set_bkg_tiles(snakex[tail], snakey[tail], 1,1, &green_tile);
   
    head = arrayi[arraylochead];
    tail = arrayi[arrayloctail];
    
  }
}

