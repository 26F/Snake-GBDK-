void rot_arrayR(UINT8 * array, UINT8 size) {
  UINT8 c; // might only be able to be 128 squares long /:
  UINT8 temp1 = array[0];
  UINT8 temp2;
  for (c = 0; c < size; c++) {
    if (c - 1 > size) {
      array[0] = array[size-1];
    }
    
    if (c > 0) {
      temp2 = array[c];
      array[c] = temp1;
      temp1 = temp2;
    }
  }
}

